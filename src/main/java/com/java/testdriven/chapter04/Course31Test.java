package com.java.testdriven.chapter04;

import org.testng.annotations.Test;

/**
 * 	注解 @Test
 * 
 * 	
 * 	JUnit和TestNG都使用注解 @Test 将方法指定为测试。
 * 	JUnit要求使用 @Test 对每个用作测试的方法进行注解，而TestNG同时允许在类级使用这个注解。
 * 	以这种方式使用该注解时，除非特别指定，否则类中所有公有方法都被视为测试：
 *	
 *	@author hzweiyongqiang
 */
@Test
public class Course31Test {

}
