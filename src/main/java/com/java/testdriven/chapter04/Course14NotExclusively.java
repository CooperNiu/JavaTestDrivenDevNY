package com.java.testdriven.chapter04;


/**
 * 	为何不只使用单元测试
 * 
 * 	
 * 	单元测试和其他测试的几个重要差别：
 * 	（一）
 * 	单元测试旨在对小型功能单元进行检查。
 * 	单元测试很简单、易于编写且运行速度很快，通常在所有测试中占据的分量最大。
 * 	单元测试的编写更容易，运行更快，提供的代码覆盖率更高。
 * 	（二）
 * 	功能测试和验收测试的职责是核实整个应用程序像预期的那样工作。
 * 	单元测试旨在检查代码的内部质量，而功能测试和验收测试用于确保整个系统在客户或用户看来能够正常工作。
 * 	（三）
 *	集成测试旨在核实各个单元、模块、应用程序乃至系统被妥善地集成在一起。
 *
 *	@author hzweiyongqiang
 */
